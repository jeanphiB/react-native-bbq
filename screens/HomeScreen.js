import React from 'react';
import {
  Image,
  Platform,
  ScrollView,
  StyleSheet,
  Text,
  TouchableOpacity,
  View,
} from 'react-native';
import { WebBrowser } from 'expo';
import { checkin } from '../actions/apicalls';

import { MonoText } from '../components/StyledText';

export default class HomeScreen extends React.Component {
  state = {
    checkin: [],
  };

  apiCalls() {
    this.setState({ loading: true });
    checkin.getStats().then(checkin => {
      this.setState({ checkin });
    });
  }

  componentDidMount() {
    const { navigation } = this.props;
    this.focusListener = navigation.addListener("didFocus", () => {
      this.apiCalls();
    });
  }

  componentWillUnmount() {
    this.focusListener.remove();
  }

  render() {
    const { checkin } = this.state;

    return (
      <View style={styles.container}>
        <ScrollView style={styles.container} contentContainerStyle={styles.contentContainer}>
          <View style={styles.welcomeContainer}>
            <Image
              source={require('../assets/images/soiree_barbecue_mojito.png')}
              style={styles.welcomeImage}
            />
          </View>

          <View style={styles.getStartedContainer}>
            {this._maybeRenderDevelopmentModeWarning()}

            {checkin && checkin.map(check => (
              <Text style={styles.getStartedText} key={check.checkin}>
                {check.checkin === '0' ? 'Pas encore là : ' : 'Présents : '}{check.count}
              </Text>
            ))}
          </View>
        </ScrollView>
      </View>
    );
  }

  _maybeRenderDevelopmentModeWarning() {
    if (__DEV__) {
      return (
        <Text style={styles.developmentModeText}>
          Development mode is enabled.
        </Text>
      );
    }
  }
}

